/**
 * Git hook installation — installs, uninstalls, and reports status of
 * managed git hooks derived from `.manifest.yml` workflow events.
 *
 * @module
 */

import * as cliParseArgs from "@std/cli/parse-args";
import * as fmtColors from "@eser/shell/formatting/colors";
import * as primitives from "@eser/primitives";
import * as standards from "@eser/standards";
import * as configManifest from "@eser/config/manifest";
import type * as shellArgs from "@eser/shell/args";

// =============================================================================
// Constants
// =============================================================================

/** Maps manifest event names to git hook filenames and extra shell args. */
const EVENT_TO_HOOK: Record<string, { hookName: string; args: string }> = {
  precommit: { hookName: "pre-commit", args: "" },
  commitmsg: { hookName: "commit-msg", args: '-- "$1"' },
  prepush: { hookName: "pre-push", args: "" },
};

/** Marker comment placed in managed hook scripts. */
const MARKER = "# @eser managed hook";

// =============================================================================
// Helpers
// =============================================================================

/**
 * Generate the shell script content for a git hook.
 *
 * Uses `getCliCommand()` to detect the best available command at generation
 * time: eser binary > npx > pnpx > bunx > deno run jsr: > "eser" fallback.
 */
const generateHookScript = async (
  event: string,
  args: string,
): Promise<string> => {
  const cliCmd = await standards.runtime.getCliCommand({
    command: "eser",
    devCommand: "deno task cli",
    npmPackage: "eser",
    jsrPackage: "@eser/cli",
  });
  const argsStr = args.length > 0 ? ` ${args}` : "";
  const command = `${cliCmd} workflows run -e ${event}${argsStr}`;

  return [
    "#!/bin/sh",
    `${MARKER} — generated by \`eser codebase install\``,
    "# Do not edit. Re-run `eser codebase install` to update.",
    "",
    command,
    "",
  ].join("\n");
};

/**
 * Locate the `.git` directory for the current repository.
 */
const findGitDir = async (): Promise<string | null> => {
  try {
    const output = await standards.runtime.current.exec.exec(
      "git",
      ["rev-parse", "--git-dir"],
    );
    const dir = output.trim();
    if (dir.length > 0) return dir;
  } catch {
    // fall through
  }

  // Fallback: check cwd
  const cwd = standards.runtime.current.process.cwd();
  const dotGit = standards.runtime.current.path.join(cwd, ".git");
  try {
    const info = await standards.runtime.current.fs.stat(dotGit);
    if (info.isDirectory) return dotGit;
  } catch {
    // not found
  }

  return null;
};

/**
 * Extract unique events from a manifest's workflows array.
 */
const extractEvents = (
  raw: Record<string, unknown>,
): string[] => {
  const workflows = raw["workflows"] as
    | readonly { on?: readonly string[] }[]
    | undefined;
  if (!Array.isArray(workflows)) return [];

  const events = new Set<string>();
  for (const wf of workflows) {
    if (Array.isArray(wf.on)) {
      for (const ev of wf.on) {
        if (typeof ev === "string") events.add(ev);
      }
    }
  }
  return [...events];
};

// =============================================================================
// Install
// =============================================================================

/**
 * Install managed git hooks based on `.manifest.yml` workflow events.
 *
 * Flags:
 *   --force     Overwrite non-managed hooks
 *   --dry-run   Show what would happen without writing
 *   --help      Show usage
 */
export const main = async (
  cliArgs?: readonly string[],
): Promise<shellArgs.CliResult<void>> => {
  const parsed = cliParseArgs.parseArgs((cliArgs ?? []) as string[], {
    boolean: ["force", "dry-run", "help"],
    alias: { h: "help", f: "force", n: "dry-run" },
  });

  if (parsed.help) {
    console.log("eser codebase install — Install git hooks from .manifest.yml");
    console.log("");
    console.log("Flags:");
    console.log("  --force, -f     Overwrite non-managed hooks");
    console.log("  --dry-run, -n   Show what would happen without writing");
    console.log("  --help, -h      Show this help");
    return primitives.results.ok(undefined);
  }

  const force = parsed.force as boolean;
  const dryRun = parsed["dry-run"] as boolean;

  // 1. Find .git directory
  const gitDir = await findGitDir();
  if (gitDir === null) {
    console.error(fmtColors.c.error("Error: not inside a git repository"));
    return primitives.results.fail({ exitCode: 1 });
  }

  const hooksDir = standards.runtime.current.path.join(gitDir, "hooks");

  // 2. Load manifest
  const cwd = standards.runtime.current.process.cwd();
  const raw = await configManifest.loadManifest(cwd);
  if (raw === null) {
    console.error(
      fmtColors.c.error(
        "Error: no .manifest.yml found in current directory",
      ),
    );
    return primitives.results.fail({ exitCode: 1 });
  }

  // 3. Extract events
  const events = extractEvents(raw);
  if (events.length === 0) {
    console.log(
      fmtColors.c.warning("No workflow events found — nothing to install."),
    );
    return primitives.results.ok(undefined);
  }

  // 4. Ensure hooks directory exists
  if (!dryRun) {
    await standards.runtime.current.fs.ensureDir(hooksDir);
  }

  // 5. Install hooks
  let installed = 0;
  let skipped = 0;
  let updated = 0;

  for (const event of events) {
    const mapping = EVENT_TO_HOOK[event];
    if (mapping === undefined) {
      console.log(
        fmtColors.c.dim(`  skip  ${event} (unknown event, no hook mapping)`),
      );
      continue;
    }

    const { hookName, args } = mapping;
    const hookPath = standards.runtime.current.path.join(hooksDir, hookName);
    const script = await generateHookScript(event, args);

    let exists = false;
    let isManaged = false;

    try {
      const existing = await standards.runtime.current.fs.readTextFile(
        hookPath,
      );
      exists = true;
      isManaged = existing.includes(MARKER);
    } catch {
      // file does not exist
    }

    if (dryRun) {
      if (!exists) {
        console.log(
          fmtColors.c.info(`  would create  ${hookName}`),
        );
      } else if (isManaged) {
        console.log(
          fmtColors.c.info(`  would update  ${hookName} (managed)`),
        );
      } else if (force) {
        console.log(
          fmtColors.c.warning(
            `  would overwrite  ${hookName} (--force)`,
          ),
        );
      } else {
        console.log(
          fmtColors.c.warning(
            `  would skip  ${hookName} (user hook; use --force to overwrite)`,
          ),
        );
      }
      continue;
    }

    if (exists && !isManaged && !force) {
      console.log(
        fmtColors.c.warning(
          `  skip  ${hookName} — user hook exists (use --force to overwrite)`,
        ),
      );
      skipped++;
      continue;
    }

    await standards.runtime.current.fs.writeTextFile(hookPath, script);

    // chmod +x (non-fatal on platforms without chmod support)
    try {
      await standards.runtime.current.fs.chmod(hookPath, 0o755);
    } catch {
      // Windows doesn't support chmod — hook will still work via shebang
    }

    if (exists && isManaged) {
      console.log(
        fmtColors.c.success(`  updated  ${hookName}`),
      );
      updated++;
    } else {
      console.log(
        fmtColors.c.success(`  installed  ${hookName}`),
      );
      installed++;
    }
  }

  // Summary
  console.log("");
  const parts: string[] = [];
  if (installed > 0) parts.push(`${installed} installed`);
  if (updated > 0) parts.push(`${updated} updated`);
  if (skipped > 0) parts.push(`${skipped} skipped`);
  if (dryRun) {
    console.log(fmtColors.c.dim("(dry run — no changes made)"));
  } else if (parts.length > 0) {
    console.log(fmtColors.c.success(`Done: ${parts.join(", ")}.`));
  } else {
    console.log(fmtColors.c.dim("Nothing to do."));
  }

  return primitives.results.ok(undefined);
};

