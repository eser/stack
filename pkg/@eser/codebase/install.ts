/**
 * Remove all managed git hooks (those containing the `@eser managed hook` marker).
 */
export const uninstallMain = async (
  cliArgs?: readonly string[],
): Promise<shellArgs.CliResult<void>> => {
  const parsed = cliParseArgs.parseArgs((cliArgs ?? []) as string[], {
    boolean: ["help"],
    alias: { h: "help" },
  });

  if (parsed.help) {
    console.log("eser codebase uninstall — Remove managed git hooks");
    return primitives.results.ok(undefined);
  }

  const gitDir = await findGitDir();
  if (gitDir === null) {
    console.error(fmtColors.c.error("Error: not inside a git repository"));
    return primitives.results.fail({ exitCode: 1 });
  }

  const hooksDir = standards.runtime.current.path.join(gitDir, "hooks");
  let removed = 0;

  // Iterate over known hook names
  const knownHooks = Object.values(EVENT_TO_HOOK).map((m) => m.hookName);
  const uniqueHooks = [...new Set(knownHooks)];

  for (const hookName of uniqueHooks) {
    const hookPath = standards.runtime.current.path.join(hooksDir, hookName);
    try {
      const content = await standards.runtime.current.fs.readTextFile(hookPath);
      if (content.includes(MARKER)) {
        await standards.runtime.current.fs.remove(hookPath);
        console.log(fmtColors.c.success(`  removed  ${hookName}`));
        removed++;
      }
    } catch {
      // file doesn't exist — skip
    }
  }

  console.log("");
  if (removed > 0) {
    console.log(fmtColors.c.success(`Done: ${removed} hook(s) removed.`));
  } else {
    console.log(fmtColors.c.dim("No managed hooks found."));
  }

  return primitives.results.ok(undefined);
};

