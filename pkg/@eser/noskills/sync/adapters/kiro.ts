// Spec Parsing & Projection
// =============================================================================

type ParsedSpec = {
  readonly title: string;
  readonly concerns: string;
  readonly discoveryAnswers: string;
  readonly contributorGuide: string;
  readonly publicApi: string;
  readonly outOfScope: string;
  readonly tasks: string;
  readonly verification: string;
};

const parseSpecMd = (content: string): ParsedSpec => {
  // Extract title from first H1
  const titleMatch = content.match(/^# Spec:\s*(.+)$/m);
  const title = titleMatch?.[1]?.trim() ?? "Untitled";

  // Split on ## headings and build a section map
  const sections = new Map<string, string>();
  const parts = content.split(/^## /m);

  for (const part of parts) {
    const newlineIdx = part.indexOf("\n");

    if (newlineIdx === -1) {
      continue;
    }

    const heading = part.slice(0, newlineIdx).trim().toLowerCase();
    const body = part.slice(newlineIdx + 1).trim();
    sections.set(heading, body);
  }

  // Match section headings (case-insensitive, prefix match)
  const findSection = (prefix: string): string => {
    for (const [key, value] of sections) {
      if (key.startsWith(prefix.toLowerCase())) {
        return value;
      }
    }

    return "";
  };

  return {
    title,
    concerns: findSection("concerns"),
    discoveryAnswers: findSection("discovery answers"),
    contributorGuide: findSection("contributor guide"),
    publicApi: findSection("public api"),
    outOfScope: findSection("out of scope"),
    tasks: findSection("tasks"),
    verification: findSection("verification"),
  };
};

const buildRequirementsMd = (spec: ParsedSpec): string => {
  const lines = [
    `# Feature: ${spec.title}`,
    "",
    "## Overview",
    "",
    spec.discoveryAnswers || "(no discovery answers yet)",
    "",
    "## Requirements",
    "",
  ];

  // Extract requirements from discovery answers — use subsection headings as items
  const reqLines = spec.discoveryAnswers.split("\n").filter((l) =>
    l.startsWith("### ")
  );

  if (reqLines.length > 0) {
    for (const line of reqLines) {
      lines.push(`- ${line.replace(/^###\s*/, "")}`);
    }
  } else if (spec.discoveryAnswers) {
    lines.push("- See overview for details");
  } else {
    lines.push("- (none yet)");
  }

  lines.push("", "## Acceptance Criteria", "");

  // Map verification items to acceptance criteria
  if (spec.verification) {
    const verLines = spec.verification.split("\n").filter((l) => l.trim());
    for (const line of verLines) {
      const trimmed = line.replace(/^[-*]\s*/, "").trim();

      if (trimmed) {
        lines.push(`- [ ] ${trimmed}`);
      }
    }
  } else {
    lines.push("- [ ] (none yet)");
  }

  lines.push("", "## Out of Scope", "");
  lines.push(spec.outOfScope || "(nothing listed)");
  lines.push("");

  return lines.join("\n");
};

const buildDesignMd = (spec: ParsedSpec): string => {
  const lines = [
    `# Design: ${spec.title}`,
    "",
    "## Architecture",
    "",
  ];

  // Extract architecture info from discovery answers
  if (spec.discoveryAnswers) {
    lines.push(spec.discoveryAnswers);
  } else {
    lines.push("(no architecture decisions yet)");
  }

  lines.push("", "## Concerns", "");

  if (spec.concerns) {
    lines.push(spec.concerns);
  } else {
    lines.push("(no active concerns)");
  }

  lines.push("", "## Decisions", "");

  // Contributor guide and public API inform design decisions
  if (spec.contributorGuide || spec.publicApi) {
    if (spec.contributorGuide) {
      lines.push("### Contributor Guide", "", spec.contributorGuide, "");
    }

    if (spec.publicApi) {
      lines.push("### Public API Surface", "", spec.publicApi, "");
    }
  } else {
    lines.push("(no decisions yet)");
  }

  lines.push("");

  return lines.join("\n");
};

const buildTasksMd = (spec: ParsedSpec): string => {
  const lines = [
    `# Tasks: ${spec.title}`,
    "",
  ];

  if (spec.tasks) {
    lines.push(spec.tasks);
  } else {
    lines.push("(no tasks yet)");
  }

  lines.push("");

  return lines.join("\n");
};

