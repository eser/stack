const buildRules = (rules: readonly string[]): string => {
  const lines = [
    "---",
    "inclusion: always",
    "---",
    "",
    "# noskills Rules",
    "",
    "# Generated by noskills — do not edit manually",
    "",
    "## Active Rules",
    "",
  ];

  for (const rule of rules) {
    lines.push(`- ${rule}`);
  }

  lines.push("");

  return lines.join("\n");
};

