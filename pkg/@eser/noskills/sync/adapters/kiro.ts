type KiroMcpServerEntry = {
  readonly command: string;
  readonly args: readonly string[];
  readonly env: Record<string, string>;
  readonly autoApprove: readonly string[];
  readonly disabled: boolean;
};

type KiroMcpConfig = {
  mcpServers: Record<string, KiroMcpServerEntry>;
};

const buildMcpConfig = (commandPrefix: string): KiroMcpConfig => {
  const parts = commandPrefix.split(/\s+/);
  const command = parts[0] ?? "npx";
  const args = [...parts.slice(1), "mcp-serve"];

  return {
    mcpServers: {
      noskills: {
        command,
        args,
        env: {},
        autoApprove: ["noskills_next", "noskills_status"],
        disabled: false,
      },
    },
  };
};

