export const kiroAdapter: adapter.ToolAdapter = {
  id: "kiro",

  capabilities: {
    rules: true,
    hooks: true,
    agents: true,
    specs: true,
    mcp: true,
    interaction: {
      hasAskUserTool: false,
      optionPresentation: "prose",
      hasSubAgentDelegation: true,
      subAgentMethod: "delegation",
    },
  },

  async syncRules(
    ctx: adapter.SyncContext,
    options?: adapter.SyncOptions,
  ): Promise<void> {
    const steeringDir = `${ctx.root}/${STEERING_DIR}`;
    await crossRuntime.runtime.fs.mkdir(steeringDir, { recursive: true });

    // 1. Protocol — always generated
    await crossRuntime.runtime.fs.writeTextFile(
      `${steeringDir}/${PROTOCOL_FILE}`,
      buildProtocol(ctx.commandPrefix, options),
    );

    // 2. Coaching — always generated
    await crossRuntime.runtime.fs.writeTextFile(
      `${steeringDir}/${COACHING_FILE}`,
      buildCoaching(ctx.commandPrefix),
    );

    // 3. Rules — only if rules exist; clean up stale file otherwise
    if (ctx.rules.length > 0) {
      await crossRuntime.runtime.fs.writeTextFile(
        `${steeringDir}/${RULES_FILE}`,
        buildRules(ctx.rules),
      );
    } else if (await fileExists(`${steeringDir}/${RULES_FILE}`)) {
      await crossRuntime.runtime.fs.remove(`${steeringDir}/${RULES_FILE}`);
    }

    // 4. Clean up legacy conventions.md from old single-file format
    if (await fileExists(`${steeringDir}/${LEGACY_FILE}`)) {
      await crossRuntime.runtime.fs.remove(`${steeringDir}/${LEGACY_FILE}`);
    }
  },

  async syncHooks(
    ctx: adapter.SyncContext,
    _options?: adapter.SyncOptions,
  ): Promise<void> {
    const hooksDir = `${ctx.root}/${HOOKS_DIR}`;
    const hooksPath = `${hooksDir}/${HOOKS_FILE}`;

    await crossRuntime.runtime.fs.mkdir(hooksDir, { recursive: true });

    // Read existing hooks config (preserve non-noskills hooks)
    let existingHooks: KiroHook[] = [];
    try {
      const content = await crossRuntime.runtime.fs.readTextFile(hooksPath);
      const parsed = JSON.parse(content) as KiroHooksConfig;
      if (Array.isArray(parsed.hooks)) {
        existingHooks = parsed.hooks;
      }
    } catch {
      // File doesn't exist yet
    }

    // Filter out previous noskills-managed hooks, keep user hooks
    const userHooks = existingHooks.filter(
      (h) => !(h as { _noskills?: boolean })._noskills,
    );

    // Build fresh noskills hooks and merge
    const noskillsConfig = buildHooksConfig(ctx.commandPrefix);
    const merged: KiroHooksConfig = {
      hooks: [...userHooks, ...noskillsConfig.hooks],
    };

    await crossRuntime.runtime.fs.writeTextFile(
      hooksPath,
      JSON.stringify(merged, null, 2) + "\n",
    );
  },

  async syncAgents(
    ctx: adapter.SyncContext,
    _options?: adapter.SyncOptions,
  ): Promise<void> {
    const agentsDir = `${ctx.root}/${AGENTS_DIR}`;
    await crossRuntime.runtime.fs.mkdir(agentsDir, { recursive: true });

    // 1. Executor agent — write tools + delegation
    const executor = buildExecutorAgent(ctx.commandPrefix);
    await crossRuntime.runtime.fs.writeTextFile(
      `${agentsDir}/${EXECUTOR_FILE}`,
      JSON.stringify(executor, null, 2) + "\n",
    );

    // 2. Verifier agent — read-only tools + sub-agent coordination
    const verifier = buildVerifierAgent();
    await crossRuntime.runtime.fs.writeTextFile(
      `${agentsDir}/${VERIFIER_FILE}`,
      JSON.stringify(verifier, null, 2) + "\n",
    );
  },

  async syncSpecs(
    ctx: adapter.SyncContext,
    specPath: string,
  ): Promise<void> {
    // Read the noskills spec.md
    let content: string;

    try {
      content = await crossRuntime.runtime.fs.readTextFile(specPath);
    } catch {
      // File doesn't exist — skip gracefully
      return;
    }

    if (content.trim() === "") {
      return;
    }

    // Derive spec name from the path: .eser/specs/<spec-name>/spec.md
    const segments = specPath.split("/");
    const specName = segments[segments.length - 2] ?? "unknown";

    // Parse the noskills spec
    const parsed = parseSpecMd(content);

    // Write Kiro's three-file format
    const specDir = `${ctx.root}/${SPECS_DIR}/${specName}`;
    await crossRuntime.runtime.fs.mkdir(specDir, { recursive: true });

    await crossRuntime.runtime.fs.writeTextFile(
      `${specDir}/requirements.md`,
      buildRequirementsMd(parsed),
    );

    await crossRuntime.runtime.fs.writeTextFile(
      `${specDir}/design.md`,
      buildDesignMd(parsed),
    );

    await crossRuntime.runtime.fs.writeTextFile(
      `${specDir}/tasks.md`,
      buildTasksMd(parsed),
    );
  },

  async syncMcp(
    ctx: adapter.SyncContext,
  ): Promise<void> {
    const settingsDir = `${ctx.root}/${HOOKS_DIR}`;
    const mcpPath = `${settingsDir}/${MCP_FILE}`;

    await crossRuntime.runtime.fs.mkdir(settingsDir, { recursive: true });

    // Read existing MCP config (preserve non-noskills servers)
    let existing: KiroMcpConfig = { mcpServers: {} };
    try {
      const content = await crossRuntime.runtime.fs.readTextFile(mcpPath);
      const parsed = JSON.parse(content) as KiroMcpConfig;
      if (parsed.mcpServers !== undefined && parsed.mcpServers !== null) {
        existing = parsed;
      }
    } catch {
      // File doesn't exist yet
    }

    // Build fresh noskills MCP entry and merge
    const noskillsConfig = buildMcpConfig(ctx.commandPrefix);
    const merged: KiroMcpConfig = {
      ...existing,
      mcpServers: {
        ...existing.mcpServers,
        ...noskillsConfig.mcpServers,
      },
    };

    await crossRuntime.runtime.fs.writeTextFile(
      mcpPath,
      JSON.stringify(merged, null, 2) + "\n",
    );
  },
};
