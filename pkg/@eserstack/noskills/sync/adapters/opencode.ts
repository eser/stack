const PLUGINS_DIR = ".opencode/plugins";
const AGENTS_DIR = ".opencode/agents";
const SKILLS_DIR = ".opencode/skills";
const CONFIG_FILE = "opencode.json";

// =============================================================================
// Hooks Content Generator (Plugin)
// =============================================================================

const buildPluginContent = (commandPrefix: string): string => {
  const lines = [
    "// Generated by noskills — do not edit manually",
    'import { execSync } from "node:child_process";',
    "",
    "const run = (event: string): void => {",
    "  try {",
    `    execSync(\`${commandPrefix} invoke-hook \${event}\`, {`,
    '      stdio: "inherit",',
    "      timeout: 10000,",
    "    });",
    "  } catch {",
    "    // Hook failure should not block the session",
    "  }",
    "};",
    "",
    "export default async () => ({",
    '  "session.created": async () => {',
    '    run("session-start");',
    "  },",
    '  "tool.execute.before": async (params: { toolName: string }) => {',
    '    if (["write", "shell"].includes(params.toolName)) {',
    '      run("pre-tool-use");',
    "    }",
    "  },",
    '  "tool.execute.after": async (params: { toolName: string }) => {',
    '    if (params.toolName === "write") {',
    '      run("post-file-write");',
    '    } else if (params.toolName === "shell") {',
    '      run("post-bash");',
    "    }",
    "  },",
    '  "session.deleted": async () => {',
    '    run("stop");',
    "  },",
    "});",
    "",
  ];

  return lines.join("\n");
};

